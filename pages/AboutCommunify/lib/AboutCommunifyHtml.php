	<h1>What is Communify?</h1>
	<hr>
                <div class="text"><p>Communify is a Task Management Tool. So you may ask what is the difference to other Task Management Tools? 
                As often the difference lays in the details. Communify has some extras build in which helps to motivate your employees. The key word here is <b>"Gamification"</b>. With gamification work get some aspects back which may have been lost in our modern business world: The feeling of achivement, the feeling to get something done.
                </p><p>
                The principle is easy, make a game out of your work. For every task you complete you earn rewards. For every contribution of an idea you earn rewards and the more you contribute and work the more rewards you will get. Very eager employees even get Awards with additional rewards.
                </p>
